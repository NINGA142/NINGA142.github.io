function mosavi()
{
  
  var alamat = document.getElementById("alamat").innerHTML
  
  var aval = document.getElementById("aval").innerHTML
  
  var dovom = document.getElementById("dovom").innerHTML
  
  document.getElementById("error").style.display = "none"

  
  if (alamat == "zarb") {
    document.getElementById("mt").innerHTML = aval*dovom
  }else if (alamat == "tagh") {
    document.getElementById("mt").innerHTML = aval/dovom
  }else if (alamat == "jaam") {
    document.getElementById("mt").innerHTML = Number(aval)+Number(dovom)
  }else if (alamat=="menh") {
    document.getElementById("mt").innerHTML = aval-dovom
  }
  let x = document.getElementById("mt").innerHTML
  
  document.getElementById("aval").innerHTML = x
  
document.getElementById("zarb").disabled = false;
document.getElementById("tagh").disabled = false;
document.getElementById("menh").disabled = false;
document.getElementById("jaam").disabled = false;
document.getElementById("numbersO").style.display = "block"
document.getElementById("numbersD").style.display = "none"
document.getElementById("numbersT").style.display = "none"
document.getElementById("error").style.display = "none"
document.getElementById("dovom").innerHTML="";
  }
